#define two sets
M = {1, 2, 3, 4, 5};
U = {2, 4, 6, 8, 10};
#set union 
print("Union of M and U is",M | U)

#set intersecton
print("intersection of M nd U is",M & U)

#set difference
print("Difference of M and U is",M - U)

#set symmetric difference
print("Symmetric difference of M and U is",M ^ U)
